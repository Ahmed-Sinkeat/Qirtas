const char *qirtas_tr(const char *en) {
    if (qirtas_app_language != 1 || !en) return en;
    for (size_t i = 0; i < G_N_ELEMENTS(tr_table); i++) {
        if (strcmp(tr_table[i].en, en) == 0) return tr_table[i].ar;
    }
    return en;
}
